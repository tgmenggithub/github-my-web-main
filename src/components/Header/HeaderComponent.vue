        hoverClass: {
            beforeMount(el, binding) {
                el.addEventListener("mouseenter", () => {
                    el.classList.add(binding.value);
                });
                el.addEventListener("mouseleave", () => {
                    el.classList.remove(binding.value);
                });
            },
        },
        // 小屏下点击菜单按钮打开关闭菜单
